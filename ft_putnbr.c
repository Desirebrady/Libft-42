#include "libft.h"

void	ft_putnbr(int nb)
{
	char nbr;

	if (nb < 0)
	{
		ft_putchar('-');
		ft_putnbr(-nb);
	}
	else if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		nbr = nb + '0';
		ft_putchar(nbr);
	}
}
