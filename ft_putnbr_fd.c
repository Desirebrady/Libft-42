#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	char nbr;
	
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		nbr = nb + '0';
		ft_putchar_fd(nbr, fd);
	}
}
